extension IntegerExtension on int {
  /// Returns the integer value as a string.
  String get stringValue => toString();

  /// Returns the integer value as a double.
  double get doubleValue => toDouble();

  /// Returns the integer value as a double.
  num get numValue => toDouble();

  /// Returns the integer value as a double.
  num get numberValue => toDouble();

  /// Returns the integer value as a double.
  double get toDoubleValue => toDouble();

  /// Returns the integer value as a double.
  num get toNumValue => toDouble();

  /// Returns the integer value as a double.
  num get toNumberValue => toDouble();
}
